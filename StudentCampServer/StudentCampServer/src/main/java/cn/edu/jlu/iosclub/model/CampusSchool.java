//报名学校表单
package cn.edu.jlu.iosclub.model;

public class CampusSchool {
	
	private String tableID;
	private String tecSchool;
	private String tecStuNum;
	
	
	public String getTecSchool() {
		return tecSchool;
	}
	public void setTecSchool(String tecSchool) {
		this.tecSchool = tecSchool;
	}
	public String getTecStuNum() {
		return tecStuNum;
	}
	public void setTecStuNum(String tecStuNum) {
		this.tecStuNum = tecStuNum;
	}
	public String getTableID() {
		return tableID;
	}
	public void setTableID(String tableID) {
		this.tableID = tableID;
	}
	
	
}
